/*
 1) Difernças entre struct e class
 1. Class são do tipo referência e struct do tipo valor,
 ou seja ao criar uma instância de uma classe, o swift cria um referência da classe na memória, assim se duas varias tiverem a instância de uma mesma classe caso mude o valor de uma a outra também sofrerá alteração, o mesmo não ocorre em um struct uma vez que cada instância tem o seu valor.
 
 2. Classes devem ter inicializadores para seus objetos, as structs não precisam.
 
 3. Classes podem herdar de outras classes, mas uma classe não pode herdar de uma struct.
 
 
 2) Relação entre classe e objeto.
    1. Classe é um modelo a ser seguido, tem as diretrizes de como criar o objeto.
    2. Objeto é algo que pode ser manipulado, que possui valores e tem seus estados deinidos, ou seja o objeto é a instância de uma classe.
 
 3) Diferença entre classe e objeto
    Classe é o modelo, e o objeto é a instancia de uma classe, o objeto contém as direttrizes da classe mas pode manipular o seus valores.
 
 */
